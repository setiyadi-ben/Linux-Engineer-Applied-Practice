public class TipeDataString {
    public static void main(String[] args) {
        // Tipe data string berisikan kumpulan karakter atau teks
        // Default value String adalah null (kosong)

        // penggunaan tipe data string harus S besar String
        String firstName = "Eko Kurniawan";
        String lastName = "Khannedy";

        System.out.println(firstName);
        System.out.println(lastName);

        // Menggabung w data String
        String fullName = firstName + " " + lastName;

        System.out.println(fullName);
    }

}
